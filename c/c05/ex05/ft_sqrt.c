#include<stdio.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	n;

	i = 1;
	n = 1;
	while (nb > 0)
	{
		nb = nb - i;
		if (nb == 0)
			return (n);
		i += 2;
		n++;
	}
	return (0);
}
